public class ComparisonTest {
    public static void main(String[] args) {
        //비교연산자: >=, > , <=, <, ==, != (결과물은 boolean 타입(true, false)으로 나옴)
        System.out.println(10 > 9); //ture
        System.out.println(10 >= 9); //ture
        System.out.println(10 < 9); //false
        System.out.println(10 <= 9); //false
        System.out.println(10 == 10); //true
        System.out.println(10 == 9); //false
        System.out.println(10 != 9); //ture
        System.out.println(10 == 9); //false
    }
}
